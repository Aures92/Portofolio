var slideIndex = 0;
showSlides();

function showSlides() {  //création d'une fonction pour changer les images toutes les 2 secondes
  var i;
  var slides = document.getElementsByClassName("mySlides");
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";
  }
  slideIndex++;
  if (slideIndex > slides.length) {slideIndex = 1}
  slides[slideIndex-1].style.display = "block";
  setTimeout(showSlides, 2000); // Change les images toutes les 2 sec
}

